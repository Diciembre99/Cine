    /**
     * Boton para guardar un usuario en el fichero
     * @param evt 
     */
    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        int age = (int) txtAge.getValue();
        String regex = "^(\\d{8})([A-HJ-NP-TV-Z])$"; //REGEX del DNI
        Pattern pattern = Pattern.compile(regex);
        String name = txtName.getText();
        String dni = txtDNI.getText();
        Matcher matcher = pattern.matcher(dni);
        
        if (matcher.matches() && !name.isBlank()) {
            Client client = new Client(name, dni, (short) age);
            File file = new File(".\\ClientesVip\\ClientesVip.txt");
            if (!file.exists()) {
                File directory = new File(".\\ClientesVip");
                if (!directory.exists()) {
                    // Si el directorio no existe, créalo
                    directory.mkdirs();
                    RandomFilesFunction.WriteFile(client, file);
                    showUserVips();
                }
            } else {
                RandomFilesFunction.WriteFile(client, file);
                showUserVips();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debes completar los campos correctamente", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed
