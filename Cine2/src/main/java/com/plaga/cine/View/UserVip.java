    /**
     * Muestra los usuarios en el multilineText
     */
    private void showUserVips() {
        File file = new File(".\\ClientesVip\\ClientesVip.txt");
        vips = RandomFilesFunction.ReadFile(file);
        int cont = 0;
        txtArea.setText("");
        for (Client vip : vips) {
            cont++;
            System.out.println(vip.toString());
            txtArea.append(cont + vip.toString());
        }
    }

